#include <stdio.h>
#include <stdlib.h>

#define COUNT 500

/* ****************************************************************** */

/***********************************************************************
    * next

***********************************************************************/
unsigned next(unsigned x) {
	return 2*x;
}


/***********************************************************************
    * next2

***********************************************************************/
unsigned next2(unsigned x) {
	if ((2*x-1) % 3 == 0)
		return (2*x-1) / 3;

	return 0;
}


/***********************************************************************
    * MAIN

***********************************************************************/
int main() {
	printf("NUM,X2,(X2-1)/3\n");
	for (unsigned i = 1; i < COUNT; i++)
		printf("%u,%u,%u\n", i, next(i), next2(i));

	return EXIT_SUCCESS;
}
